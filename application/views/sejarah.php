                    <h3 class="text-justify text-bold text-center">Baznas Purwakarta</h3>
                    <p style="text-align: justify;">BAZNAS terbentuk di saat Pemerintah Republik Indonesia telah menerbitkan dukungan terhadap pengelolaan zakat ditunjukkan dengan menerbitkan Peraturan Menteri Agama No. 4 Tahun 1968 tentang Pembentukan Badan Amil Zakat dan Peraturan Menteri Agama No 5 Tahun 1968 tentang Pembentukan Baitul Maal di tingkat pusat, provinsi dan kabupaten/kotamadya. Namun demikian peraturan menteri tersebut tidak ada tindaklanjut, yang terjadi pada akhirnya teranulir dengan lahirnya Instruksi Menteri Agama No 1 Tahun 1969, yang menyatakan pelaksanaan Peraturan Menteri Agama No 4 dan No 5 Tahun 1968 ditunda sampai batas waktu yang tidak ditentukan.
                        Keberadaan pengelola zakat semi-pemerintah secara nasional dikukuhkan dengan Surat Keputusan Bersama (SKB) Menteri Dalam Negeri dan Menteri Agama No. 29 dan No. 47 Tahun 1991 tentang Pembinaan BAZIS yang diterbitkan teri Agama dan Menteri Dalam Negeri setelah melalui Musyawarah Nasional MUI IV tahun 1990. Langkah tersebut juga diikuti dengan dikeluarkan juga Instruksi Men-teri Agama No. 5 Tahun 1991 tentang Pembinaan Teknis BAZIS sebagai aturan pelaksanaannya.Baru pada tahun 1999, pemerintah melahirkan Undang-Undang Nomor 38 Tahun 1999 tentang Pengelolaan Zakat. Dalam Undang-Undang tersebut diakui adanya dua jenis organisasi pengelola zakat yaitu Badan Amil Zakat (BAZ) yang dibentuk pemerintah dan Lembaga Amil Zakat (LAZ) yang dibentuk oleh masyarakat dan dikukuhkan oleh pemerintah. BAZ terdiri dari BAZNAS pusat, BAZNAS Propinsi, dan BAZNAS kabupaten/kota. Sebagai implementasi UU Nomor 38 Tahun 1999 dibentuk Badan Amil Zakat Nasional (BAZNAS) dengan Surat Keputusan Presiden Republik Indonesia Nomor 8 Tahun 2001. Dalam Surat Keputusan ini disebutkan tugas dan fungsi BAZNAS yaitu untuk melakukan penghimpunan dan pendayagunaan zakat. Langkah awal adalah mengupayakan memudahkan pelayanan, BAZNAS menerbitkan nomor pokok wajib zakat (NPWZ) dan bukti setor zakat (BSZ).Pada tanggal 27 Oktober 2011, Pemerintah dan DPR RI menyetujui Undang-undang pengelolaan zakat pengganti Undang-Undang Nomor 38 Tahun 1999 yang kemudian diundangkan sebagai UU Nomor 23 Tahun 2011 pada tanggal 25 November 2011. UU ini menetapkan bahwa pengelolaan zakat bertujuan (1) meningkatkan efektivitas dan efisiensi pelayanan dalam pengelolaan zakat dan (2) meningkatkan manfaat zakat untuk mewujudkan kesejahteraan masyarakat dan penanggulangan kemiskinan. UU mengatur bahwa kelembagaan pengelola zakat harus terintegrasi dengan BAZNAS sebagai koordinator seluruh pengelola zakat, baik BAZNAS Provinsi, BAZNAS Kabupaten/Kota maupun LAZ.BAZNAS Kabupaten purwakarta aktifitas kelembagaannya berdasarkan Peraturan Daerah Kabupaten purwakarta Nomor 03/2007 tentang Pengelolaan Zakat, Surat Keputusan Bupati Purwakarta Nomor 451.12/Kep.726-bKesra/2016 tentang Penetapan Pimpinan BaZNAS Kabupaten Purwakarta masa bakti 2016/2021, Surat Edaran Bupati Purwakarta Nomor 451.12/2607/2014 tentang optimalisasi pengumpulan dan pendayagunaan zakat di kabupaten Purwakarta, Himbauan Bupati Purwakarta Nomor 451.12/1368/dpkad tentang pengurangan pajak bagi wajib zakat pengusaha dan Surat Edaran Bupati Purwakarta Nomor 466/1655-Kesra/2014 tentang Gerakan Seribu Rupiah. Payung hukum di atas menjadi landasan bagi BAZNAS Kabupaten Purwakarta melakukan fungsi perancangan pengumpulan, pengelolaan dan penyaluran sesuai dengan peraturan perundang-undangan yang berlaku.</p>
